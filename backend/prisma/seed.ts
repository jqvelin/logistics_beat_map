async function main(): Promise<void> {
  const passwordHash = await bcrypt.hash('password123', 10);

  await prisma.emailLoginCode.deleteMany();
  await prisma.progress.deleteMany();
  await prisma.userAchievement.deleteMany();
  await prisma.task.deleteMany();
  await prisma.lesson.deleteMany();
  await prisma.module.deleteMany();
  await prisma.course.deleteMany();
  await prisma.user.deleteMany();

  const user1 = await prisma.user.create({
    data: {
      email: 'student@example.com',
      password: passwordHash,
      xp: 100,
      level: 2,
      streak: 3,
    },
  });

  await prisma.user.create({
    data: {
      email: 'learner@example.com',
      password: passwordHash,
      xp: 0,
      level: 1,
      streak: 0,
    },
  });

  let firstTaskId: string | null = null;

  for (const course of coursesData) {
    const created = await prisma.course.create({
      data: {
        title: course.title,
        modules: {
          create: course.modules.map((mod) => ({
            title: mod.title,
            lessons: {
              create: mod.lessons.map((lesson) => ({
                title: lesson.title,
                tasks: {
                  create: lesson.tasks.map((t) => ({
                    type: t.type,
                    content: t.content as unknown as Prisma.InputJsonValue,
                  })),
                },
              })),
            },
          })),
        },
      },
      include: {
        modules: {
          include: {
            lessons: {
              include: { tasks: true },
            },
          },
        },
      },
    });

    if (!firstTaskId) {
      const t = created.modules[0]?.lessons[0]?.tasks[0];
      if (t) firstTaskId = t.id;
    }
  }

  if (firstTaskId) {
    await prisma.progress.create({
      data: {
        userId: user1.id,
        taskId: firstTaskId,
        completed: true,
        completedAt: new Date(),
      },
    });
  }

  const achievementDefs = [
    {
      key: 'first_lesson',
      title: 'Первые шаги',
      description: 'Полностью завершите первый учебный урок',
      emoji: '🎯',
      sortOrder: 0,
    },
    {
      key: 'week_streak',
      title: 'Первая пройденная неделя',
      description: 'Выполняйте задания 7 дней подряд',
      emoji: '🏅',
      sortOrder: 1,
    },
    {
      key: 'perfect_lesson',
      title: 'Идеальный результат',
      description: 'Соберите первый полный урок без ошибок в тестах',
      emoji: '⭐',
      sortOrder: 2,
    },
  ] as const;

  for (const def of achievementDefs) {
    await prisma.achievement.upsert({
      where: { key: def.key },
      update: {
        title: def.title,
        description: def.description,
        emoji: def.emoji,
        sortOrder: def.sortOrder,
      },
      create: {
        key: def.key,
        title: def.title,
        description: def.description,
        emoji: def.emoji,
        sortOrder: def.sortOrder,
      },
    });
  }

  const taskCount = await prisma.task.count();
  const lessonCount = await prisma.lesson.count();
  const moduleCount = await prisma.module.count();
  const courseCount = await prisma.course.count();

  console.log(
    `Готово: курсов ${courseCount}, модулей ${moduleCount}, уроков ${lessonCount}, заданий ${taskCount}.`,
  );
}

main()
  .then(async () => {
    await prisma.$disconnect();
  })
  .catch(async (e) => {
    console.error(e);
    await prisma.$disconnect();
    process.exit(1);
  });
