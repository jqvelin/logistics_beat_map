const prisma = new PrismaClient();

type QuizContent = {
  type: 'quiz';
  question: string;
  options: string[];
  answer: number;
};

type TheoryContent = {
  type: 'theory';
  text: string;
};

type SeedTask =
  | { type: 'quiz'; content: QuizContent }
  | { type: 'theory'; content: TheoryContent };

type SeedLesson = { title: string; tasks: SeedTask[] };
type SeedModule = { title: string; lessons: SeedLesson[] };
type SeedCourse = { title: string; modules: SeedModule[] };

function quiz(question: string, options: string[], answer: number): SeedTask {
  if (options.length < 2 || options.length > 8) {
    throw new Error(`Quiz must have 2–8 options, got ${options.length}`);
  }
  if (answer < 0 || answer >= options.length) {
    throw new Error('Quiz answer index out of range');
  }
  return {
    type: 'quiz',
    content: { type: 'quiz', question, options, answer },
  };
}

function theory(text: string): SeedTask {
  return { type: 'theory', content: { type: 'theory', text } };
}

